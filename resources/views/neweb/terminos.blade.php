      <h3>1. Objeto del Servicio</h3>
      <p>El presente contrato tiene como finalidad el alquiler temporal de un vehículo de propiedad de Rent a Car Pasto, bajo las condiciones aquí estipuladas.</p>

      <h3>2. Estado del Vehículo</h3>
      <p>El arrendatario declara haber recibido el vehículo en óptimas condiciones mecánicas y estéticas, según el acta de entrega. Deberá devolverlo en el mismo estado, con el tanque de combustible al nivel recibido.</p>

      <h3>3. Custodia y Responsabilidad</h3>
      <p>El arrendatario es responsable total del vehículo durante el tiempo de alquiler, tanto en su custodia como en su uso. Será responsable por cualquier daño, pérdida o infracción de tránsito ocurrida durante este periodo.</p>

      <h3>4. Uso del Vehículo</h3>
      <p>No se permite sacar el vehículo del territorio colombiano sin autorización escrita. El arrendatario debe cumplir todas las normas de tránsito y responder por infracciones o sanciones que ocurran durante la vigencia del contrato.</p>

      <h3>5. Garantía y Depósito</h3>
      <p>El cliente autoriza la retención temporal de un valor en su tarjeta de crédito o depósito como garantía. Dicho valor se libera dentro de los 6 días hábiles posteriores a la devolución, una vez verificado que no existan multas o daños pendientes.</p>

      <h3>6. Cobertura de Seguros y Deducibles</h3>
      <p>El vehículo cuenta con póliza de seguro. En caso de accidente, siniestro o pérdida, el arrendatario asumirá el deducible correspondiente (10% del valor comercial del vehículo, o 20% si es de servicio público).</p>

      <h3>7. Daños y Reparaciones</h3>
      <p>Las reparaciones solo podrán realizarse en talleres autorizados por la aseguradora o por Rent a Car Pasto. Si el daño no supera los cuatro salarios mínimos legales, el costo correrá por cuenta del arrendatario.</p>

      <h3>8. Responsabilidad Legal</h3>
      <p>En caso de retención o inmovilización del vehículo por causas imputables al usuario, este asumirá todos los costos derivados, incluyendo el lucro cesante mientras el vehículo no esté disponible para alquiler.</p>

      <h3>9. Tratamiento de Datos Personales</h3>
      <p>Al firmar o aceptar el servicio, el cliente autoriza el uso de sus datos personales conforme a la Ley 1581 de 2012 para fines administrativos, de contacto y gestión del servicio.</p>

      <h3>10. Vigencia</h3>
      <p>El arrendamiento tendrá vigencia por el periodo acordado en el contrato, contado desde la entrega del vehículo hasta su devolución formal a Rent a Car Pasto.</p>

      <h3>11. Aceptación</h3>
      <p>El uso del servicio implica la aceptación plena de los presentes Términos y Condiciones. Cualquier modificación será publicada y notificada oportunamente.</p>

